-- Dynamic monthly incident catalog. These tables intentionally keep random
-- incidents separate from historical officer life events.

PRAGMA foreign_keys = ON;

CREATE TABLE dynamic_event_templates (
    id TEXT PRIMARY KEY,
    event_kind TEXT NOT NULL CHECK (event_kind IN (
        'Famine',
        'NaturalDisaster',
        'PublicOrder',
        'Economy',
        'Military',
        'Diplomacy',
        'Opportunity'
    )),
    polarity TEXT NOT NULL CHECK (polarity IN ('Positive', 'Negative')),
    severity TEXT NOT NULL CHECK (severity IN ('Info', 'Important', 'Warning', 'Critical')),
    event_scope TEXT NOT NULL CHECK (event_scope IN ('Player', 'World')),
    target_scope TEXT NOT NULL CHECK (target_scope IN ('PlayerCity', 'AiCity', 'AnyCity', 'BorderCity')),
    base_weight INTEGER NOT NULL CHECK (base_weight > 0),
    target_cooldown_turns INTEGER NOT NULL CHECK (target_cooldown_turns >= 0),
    deadline_turns INTEGER NOT NULL CHECK (deadline_turns BETWEEN 1 AND 6),
    enabled INTEGER NOT NULL CHECK (enabled IN (0, 1)),
    title TEXT NOT NULL,
    summary TEXT NOT NULL,
    detail TEXT NOT NULL
);

CREATE TABLE dynamic_event_conditions (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    template_id TEXT NOT NULL REFERENCES dynamic_event_templates(id) ON DELETE CASCADE,
    condition_kind TEXT NOT NULL CHECK (condition_kind IN (
        'CityFoodBelowPopulationThreshold',
        'CityFoodAtLeast',
        'CityOrderAtMost',
        'CityOrderAtLeast',
        'CityPopulationAtLeast',
        'CityTroopsAtLeast',
        'CityDefenseAtLeast',
        'CityTrainingAtMost',
        'CityAgricultureAtLeast',
        'CityCommerceAtLeast',
        'MonthRange',
        'AdjacentEnemy'
    )),
    min_value INTEGER,
    max_value INTEGER,
    sort_order INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE dynamic_event_weight_modifiers (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    template_id TEXT NOT NULL REFERENCES dynamic_event_templates(id) ON DELETE CASCADE,
    condition_kind TEXT NOT NULL CHECK (condition_kind IN (
        'CityFoodBelowPopulationThreshold',
        'CityFoodAtLeast',
        'CityOrderAtMost',
        'CityOrderAtLeast',
        'CityPopulationAtLeast',
        'CityTroopsAtLeast',
        'CityDefenseAtLeast',
        'CityTrainingAtMost',
        'CityAgricultureAtLeast',
        'CityCommerceAtLeast',
        'MonthRange',
        'AdjacentEnemy'
    )),
    min_value INTEGER,
    max_value INTEGER,
    multiplier_percent INTEGER NOT NULL CHECK (multiplier_percent > 0),
    sort_order INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE dynamic_event_choices (
    template_id TEXT NOT NULL REFERENCES dynamic_event_templates(id) ON DELETE CASCADE,
    choice_id TEXT NOT NULL,
    label TEXT NOT NULL,
    description TEXT NOT NULL,
    is_default INTEGER NOT NULL CHECK (is_default IN (0, 1)),
    sort_order INTEGER NOT NULL DEFAULT 0,
    PRIMARY KEY (template_id, choice_id)
);

CREATE TABLE dynamic_event_choice_requirements (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    template_id TEXT NOT NULL,
    choice_id TEXT NOT NULL,
    requirement_kind TEXT NOT NULL CHECK (requirement_kind IN (
        'CityGold',
        'CityFood',
        'CityMaterials',
        'CityTroops',
        'CityOrder',
        'CityPopulation'
    )),
    amount INTEGER NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    FOREIGN KEY (template_id, choice_id)
        REFERENCES dynamic_event_choices(template_id, choice_id)
        ON DELETE CASCADE
);

CREATE TABLE dynamic_event_choice_effects (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    template_id TEXT NOT NULL,
    choice_id TEXT NOT NULL,
    effect_kind TEXT NOT NULL CHECK (effect_kind IN (
        'CityGold',
        'CityFood',
        'CityMaterials',
        'CityOrder',
        'CityPopulation',
        'CityPopulationPercent',
        'CityTraining',
        'CityAgriculture',
        'CityCommerce',
        'CityDefense',
        'CityTroops',
        'CityTroopsPercent',
        'CityWoundedTroops',
        'DiplomaticScore'
    )),
    amount INTEGER NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    FOREIGN KEY (template_id, choice_id)
        REFERENCES dynamic_event_choices(template_id, choice_id)
        ON DELETE CASCADE
);

CREATE INDEX idx_dynamic_event_templates_enabled ON dynamic_event_templates(enabled, target_scope);
CREATE INDEX idx_dynamic_event_conditions_template ON dynamic_event_conditions(template_id);
CREATE INDEX idx_dynamic_event_weight_modifiers_template ON dynamic_event_weight_modifiers(template_id);
CREATE INDEX idx_dynamic_event_choices_template ON dynamic_event_choices(template_id);
CREATE INDEX idx_dynamic_event_choice_requirements_choice ON dynamic_event_choice_requirements(template_id, choice_id);
CREATE INDEX idx_dynamic_event_choice_effects_choice ON dynamic_event_choice_effects(template_id, choice_id);

