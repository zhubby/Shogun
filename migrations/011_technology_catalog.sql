CREATE TABLE technologies (
    id TEXT PRIMARY KEY,
    branch TEXT NOT NULL CHECK (branch IN ('Military', 'Domestic')),
    name TEXT NOT NULL,
    turns INTEGER NOT NULL CHECK (turns > 0 AND turns <= 24),
    gold_cost INTEGER NOT NULL CHECK (gold_cost >= 0),
    effect_summary TEXT NOT NULL,
    icon_id TEXT NOT NULL,
    sort_order INTEGER NOT NULL UNIQUE,
    ai_priority INTEGER NOT NULL UNIQUE CHECK (ai_priority > 0)
);

CREATE TABLE technology_prerequisites (
    technology_id TEXT NOT NULL REFERENCES technologies(id) ON DELETE CASCADE,
    prerequisite_id TEXT NOT NULL REFERENCES technologies(id),
    sort_order INTEGER NOT NULL,
    PRIMARY KEY (technology_id, prerequisite_id),
    CHECK (technology_id != prerequisite_id)
);

CREATE TABLE technology_effects (
    technology_id TEXT NOT NULL REFERENCES technologies(id) ON DELETE CASCADE,
    effect_kind TEXT NOT NULL CHECK (effect_kind IN (
        'city_gold_income',
        'city_food_income',
        'city_materials_income',
        'city_gold_percent',
        'city_food_percent',
        'city_materials_percent',
        'city_population_growth',
        'city_troop_recovery',
        'city_order',
        'city_training',
        'city_defense',
        'city_upkeep_reduction_percent',
        'city_food_upkeep_reduction_percent',
        'training_command_bonus',
        'development_bonus',
        'agriculture_development_bonus',
        'commerce_development_bonus',
        'order_development_bonus',
        'recruit_gold_discount_percent',
        'travel_month_reduction',
        'attack_percent',
        'defense_percent',
        'siege_attack_percent',
        'battle_loss_reduction_percent',
        'retreat_survival_percent',
        'research_cost_reduction_percent'
    )),
    amount INTEGER NOT NULL,
    sort_order INTEGER NOT NULL,
    PRIMARY KEY (technology_id, sort_order)
);

CREATE INDEX idx_technologies_branch ON technologies(branch, sort_order);
CREATE INDEX idx_technologies_ai_priority ON technologies(ai_priority);
CREATE INDEX idx_technology_prerequisites_technology ON technology_prerequisites(technology_id, sort_order);
CREATE INDEX idx_technology_prerequisites_prerequisite ON technology_prerequisites(prerequisite_id);
CREATE INDEX idx_technology_effects_technology ON technology_effects(technology_id, sort_order);

